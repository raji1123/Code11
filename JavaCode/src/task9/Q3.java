package task9;

public class Q3 {

	public static void main(String[] args) {
		// print the pyramid number pattern
		int n=4;
		int Count=1;
		for(int i=1;i<=n;i++) { //i is the row
			
			
			for(int j=1;j<=i;j++) {
				System.out.print(Count++);
			if(j!=i) {
				System.out.print(" ");//j is the col

			}}
			
			System.out.println(" ");
		
		
		
		
		

	

}}}
